char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int i;
	int j;

	i = 0;
	j = n;
	while ((n - j) < n)
	{
		dest[i] = src[n - j];
		i++;
		j--;
	}
	return (dest);
}
