int		ft_antidote(int i, int j, int k)
{
	if ((i < j && j < k) || (j < i && k < j))
		return (j);
	else if ((i < j && k < i) || (j < i && i < k))
		return (i);
	else if ((k < j && i < k) || (j < k && k < i))
		return (k);
	else
		return (0);
}
